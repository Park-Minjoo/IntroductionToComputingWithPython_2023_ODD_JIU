# Define the list
n = [1, 3, 5, 7, 99, 97, 95, 93, 91]

# Calculate the number of items in the list
item_count = len(n)

# Calculate the sum of all items in the list
item_sum = sum(n)

# Print the number of items and the sum
print("Number of items:", item_count)
print("Sum of all items:", item_sum)

# Reverse and print the list
n.reverse()
print("Reversed list:", n)
