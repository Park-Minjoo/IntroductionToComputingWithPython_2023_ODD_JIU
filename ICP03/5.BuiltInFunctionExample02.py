# Using eval() to evaluate expressions and get results
print(eval("'hi' + 'a'"))  # 'hia'
print(eval('1 + 1'))       # 2

# Using float() to convert to a floating-point number
print(float(3))   # 3.0
print(float(10))  # 10.0

# Using int() to convert to an integer and printing the results
print(int(3.0))  # 3
print(int(5.5))  # 5

# Using format() to format values into strings
print("I want {0} apples".format(3))  # "I want 3 apples"
