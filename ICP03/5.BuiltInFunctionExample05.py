# Using reversed() to reverse the order of elements in a tuple and printing the result
t = ('a', 'b', 'c')
print(list(reversed(t)))  # ['c', 'b', 'a']

# Using round() to round numbers and printing the results
print(round(5.4))  # 5
print(round(5.5))  # 6

# Using sorted() to sort a list and printing the result
a = [2, 4, 1, 9, 100]
print(sorted(a))  # [1, 2, 4, 9, 100]
