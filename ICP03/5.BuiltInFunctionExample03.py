# Using len() to get the length of strings and printing the results
print(len("Hello Python"))  # 12
print(len("piano"))         # 5

# Using list() to create a list from a tuple and printing the result
fruit = ('apple', 'strawberry', 'lemon')
print(list(fruit))  # ['apple', 'strawberry', 'lemon']

# Using max() to get the maximum element of a list and printing the result
b = [10, 2, 6, 8]
print(max(b))  # 10
