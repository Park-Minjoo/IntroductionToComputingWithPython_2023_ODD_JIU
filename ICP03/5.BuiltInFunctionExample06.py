# Using sum() to calculate the sum of elements in a list and printing the results
a = [3, 8, 6]
print(sum(a))  # 17

b = [3, 6, 1, 8, 9]
print(sum(b))  # 27

# Using tuple() to create a tuple from a list and printing the result
alist = [1, 2, 'a', 'b']
print(tuple(alist))  # (1, 2, 'a', 'b')

# Using str() to convert values to strings and printing the results
print(str(3 + 9))  # '12'
print(str(5.0))    # '5.0'
