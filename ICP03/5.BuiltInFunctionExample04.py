# Using min() to get the minimum element of a list and printing the result
a = [6, 2, 3, 10]
print(min(a))  # 2

# Using ord() to get the Unicode code point of a character and printing the results
print(ord('B'))  # 66
print(ord('b'))  # 98

# Using pow() to calculate the power and printing the results
print(pow(4, 2))  # 16
print(pow(2, 3))  # 8
