# Using abs() to get the absolute value
print(abs(-3)) # 3
print(abs(-10)) # 10

# Using ascii() to get a printable representation of a string
print(ascii('Python')) # 'Python'
print(ascii('banana')) # 'banana'

# Using chr() to get the character representation of a Unicode code point
print(chr(97)) # a
print(chr(80)) # P