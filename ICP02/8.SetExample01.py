setA = set('apples')
print(setA) # {'l', 'p', 's', 'e', 'a'}

setL = set(['Minjoo', 'Minjoo', 'Dany', 'Lindong', 'Isabella'])
print(setL) # {'Isabella', 'Lindong', 'Dany', 'Minjoo'}

setT = set(('Nacho', 'pasta', 'steak', 'chicken'))
print(setT) # {'pasta', 'steak', 'chicken', 'Nacho'}

setD = set({'one': 'satu', 'two': 'dua', 'three':'tiga'})
